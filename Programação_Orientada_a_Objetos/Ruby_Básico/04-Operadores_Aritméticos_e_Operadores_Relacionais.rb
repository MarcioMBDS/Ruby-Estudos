# +, -, *, /, **, %
# %(modulo) = resto da divisão, ou seja, o que sobra da divisão. Exemplo: 10 % 3 = 1, pois 3 cabe 3 vezes em 10, sobrando 1.
# **(exponenciação) = eleva um número a uma potência. Exemplo: 2 ** 3 = 8, pois 2 elevado à potência de 3 é igual a 8.

# Operadores Relacionais: >, <, >=, <=, ==, !=
# > (maior que) = verifica se o valor à esquerda é maior que o valor à direita. Exemplo: 5 > 3 retorna true.
# < (menor que) = verifica se o valor à esquerda é menor que o valor à direita. Exemplo: 2 < 4 retorna true.
# >= (maior ou igual a) = verifica se o valor à esquerda é maior ou igual ao valor à direita. Exemplo: 5 >= 5 retorna true.
# <= (menor ou igual a) = verifica se o valor à esquerda é menor ou igual ao valor à direita. Exemplo: 3 <= 4 retorna true.
# == (igual a) = verifica se o valor à esquerda é igual ao valor à direita. Exemplo: 5 == 5 retorna true.
# != (diferente de) = verifica se o valor à esquerda é diferente do valor à direita. Exemplo: 5 != 3 retorna true.

# Exemplo de uso dos operadores aritméticos e relacionais:
a = 10
b = 5

puts a + b       # Soma: 15
puts a - b       # Subtração: 5
puts a * b       # Multiplicação: 50
puts a / b       # Divisão: 2
puts a ** b      # Exponenciação: 100000
puts a % b       # Módulo: 0

puts a > b        # Maior que: true
puts a < b        # Menor que: false
puts a >= b       # Maior ou igual a: true
puts a <= b       # Menor ou igual a: false
puts a == b       # Igual a: false
puts a != b       # Diferente de: true
puts a == 10       # Igual a: true
puts b != 5        # Diferente de: false
